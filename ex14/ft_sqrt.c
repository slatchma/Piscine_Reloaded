int		ft_sqrt(int nb)
{
	int i;

	if (nb < 0)
		return (0);
	i = 1;
	while (i <= (nb / 2) + 1)
	{
		if ((nb / i) == i && (nb % i) == 0)
			return (i);
		i++;
	}
	return (0);
}
