#include <stdlib.h>

int		*ft_range(int min, int max)
{
	int i;
	int *tab;

	i = max - min;
	if (i <= 0)
		return (NULL);
	tab = (int*)malloc(sizeof(int) * i);
	if (tab == NULL)
		return (0);
	i = 0;
	while (min < max)
	{
		tab[i] = min;
		i++;
		min++;
	}
	return (tab);
}
